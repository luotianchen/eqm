package start.putdeco;

public class putdecopost {
    private String deconame;
    private String edeconame;
    private String delicense;
    private String time;
    private String orgcode;
    private String code;
    public putdecopost(){
        super();
    }

    public String getDeconame() {
        return deconame;
    }

    public void setDeconame(String deconame) {
        this.deconame = deconame;
    }

    public String getEdeconame() {
        return edeconame;
    }

    public void setEdeconame(String edeconame) {
        this.edeconame = edeconame;
    }

    public String getDelicense() {
        return delicense;
    }

    public void setDelicense(String delicense) {
        this.delicense = delicense;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getOrgcode() {
        return orgcode;
    }

    public void setOrgcode(String orgcode) {
        this.orgcode = orgcode;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
